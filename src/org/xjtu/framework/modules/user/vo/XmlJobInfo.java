package org.xjtu.framework.modules.user.vo;

public class XmlJobInfo {

	private String cameraName;
	private String filePath;
	private String frameRange;
	private String unitsNumber;
	private String frameNumbers;
	
	public String getCameraName() {
		return cameraName;
	}
	public void setCameraName(String cameraName) {
		this.cameraName = cameraName;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public String getFrameRange() {
		return frameRange;
	}
	public void setFrameRange(String frameRange) {
		this.frameRange = frameRange;
	}
	public String getUnitsNumber() {
		return unitsNumber;
	}
	public void setUnitsNumber(String unitsNumber) {
		this.unitsNumber = unitsNumber;
	}
	public String getFrameNumbers() {
		return frameNumbers;
	}
	public void setFrameNumbers(String frameNumbers) {
		this.frameNumbers = frameNumbers;
	}
	
}
